    private void adminPassWordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_adminPassWordKeyPressed
       if(evt.getKeyCode()==KeyEvent.VK_RIGHT){
           try{
               Conn.Database.getDatabaseConnection();
               PreparedStatement ps=Conn.Database.con.prepareStatement("Select adminid from admin where section =? and adminName=binary ? AND adminpassword=binary ? ");
               ps.setString(1, section.getSelectedItem().toString());
               ps.setString(2, adminname.getText());
               ps.setString(3,adminPassWord.getText());
               ResultSet rs=ps.executeQuery();
               if(rs.next()){
                   JOptionPane.showMessageDialog(this, "Do necessary Mr. Admin !       ");
                   timer.cancel();
                   switch (section.getSelectedIndex()) {
                       case 0:
                           DataManagement data=new DataManagement();
                           data.setVisible(true);
                           data.setLocationRelativeTo(null);
                           this.dispose();
                           School.sectionname=section.getSelectedItem().toString();
                           break;
                       case 1:
                           Exammanagement e=new Exammanagement();
                           e.setVisible(true);
                           e.setLocationRelativeTo(null);
                           this.dispose();
                           School.sectionname=section.getSelectedItem().toString();
                           break;
                       case 2:
                           SalaryManagement s=new SalaryManagement();
                           s.setVisible(true);
                           s.setLocationRelativeTo(null);
                           this.dispose();
                           School.sectionname=section.getSelectedItem().toString();
                           break;
                       default:
                           break;
                   }
               }else{
                  JOptionPane.showMessageDialog(this, "You are not welcomed !!!    ");
                  System.exit(0);
               }
           
           }catch(HeadlessException x){
               JOptionPane.showMessageDialog(this, "Error:\n"+x);
           }catch(SQLException ex){
               JOptionPane.showMessageDialog(this,"Error In SQL: \n"+ex);
           }  
       }
    }//GEN-LAST:event_adminPassWordKeyPressed

