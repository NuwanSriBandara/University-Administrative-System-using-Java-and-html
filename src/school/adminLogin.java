    public void stopwatch(){
        int delay =1000;
        int period =1000;
        timer=new Timer();
        interval= 30;
        timer.scheduleAtFixedRate(new TimerTask() {
        public void run() {
            setInterval();
        }
    }, delay, period);
    }
    public int setInterval() {
    if (interval == 1){
        timer.cancel();
    System.exit(0);}
    return --interval;
        
}
